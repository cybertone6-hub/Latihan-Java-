public class pemgulanganwhile {
    
    public static void main(String[]args) {
        System.out.println("perulangan 1-10");
        int i = 1 ;
        while(i <= 10)
        {
            System.out.print("\t"+i);
            i++;
        }
        System.out.println("\nperulangan 10-1");
        i = 10;
        while(i >= 1)
        {
            System.out.print("\t"+i);
            i--;
        }
    }
    
}
